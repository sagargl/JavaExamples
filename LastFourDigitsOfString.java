
public class LastFourDigitsOfString {

	public static void main(String[] args) {
		/* Last 4 characters of string*/
		String s="teacher";
		int size=s.length();
		System.out.println(s.substring(size-4));
		
		/*First 4 charracters of string*/
		
		String s1="Principal";
		System.out.println(s1.substring(0, 4));
	}
}
